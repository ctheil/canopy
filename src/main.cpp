///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// INCLUDES
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#include "./Comms/Comms.h"
#include "./SoilMoistureSensor/SoilMoistureSensor.h"
#include "Arduino.h"

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// GLOBALS
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int loopCount = 0;
int constexpr numSensors = 1;
Comms c;
SoilMoistureSensor sensors[numSensors] = {SoilMoistureSensor(32, "001", c)};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// MAIN
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void setup()
{
  Serial.begin(115200);

  log_i();
  log_i("setup, ESP.getSdkVersion(): ");
  log_i("%s", ESP.getSdkVersion());

  log_i("\nInit comms");
  c.setupWifi();
  c.setupMqtt();
}

void loop()
{
  // publish ever 15 minutes
  if (loopCount >= 60 * 15)
  {
    // loop over every sensor and publish to its topic id
    for (int i = 0; i != numSensors; ++i)
    {

      const int moisturePercentage = sensors[i].getMoisturePercentage();
      char buffer[64];
      snprintf(buffer, sizeof(buffer), "{\"soil-moisture\": %d}",
               moisturePercentage);
      sensors[i].publish(buffer);

      loopCount = 0;
    }
  }
  ++loopCount;
  delay(1000);
}
